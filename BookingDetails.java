public interface
BookingDetails {
    /**
     * Retrieves the date of the activity.
     *
     * @return A string representing the date of the activity.
     */
    String getActivityDate();

    /**
     * Retrieves the name of the participant in the activity.
     *
     * @return The participant's name as a string.
     */
    String getParticipantName();
}
